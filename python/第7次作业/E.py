'''
题目描述
角谷猜想:
日本一位中学生发现一个奇妙的“定理”，请角谷教授证明，而教授无能为力，于是产生角谷猜想。
猜想的内容是：任给一个自然数，若为偶数除以2，若为奇数则乘3加1，得到一个新的自然数后按照上面的法则继续演算，
若干次后得到的结果必然为1。请编程验证。
输入
任一正整数
输出
演算的过程
样例输入
10
样例输出
10/2=5
5*3+1=16
16/2=8
8/2=4
4/2=2
2/2=1
'''
n=int(input())
while(n!=1):
    n_temp = n
    if(n%2):
        n=n_temp*3+1
        print('{}*3+1={}'.format(int(n_temp),int(n)))
    else:
        n=n_temp/2
        print('{}/2={}'.format(int(n_temp),int(n)))